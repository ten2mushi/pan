# ---------------------------------------------------------------------------
# Compound "hero" renders — the new features (perceptual colour, cinematic bloom,
# harmonic comb, PCA fingerprint) stacked into a curated set. These OVERRIDE the
# plain grid entries of the same name, so `render.py <input>_<mode>` yields the
# styled version. Treatment: per-track palette (muted, no rainbow), saturation
# driven by tonality (tonal→colour, breathy→grey), ACES-tonemapped bloom, and
# long soft edge trails (edge_life 60 → ~20 s exponential fade) on the artistic
# modes. Helix is intentionally excluded from the artistic set.
# ---------------------------------------------------------------------------
ARTISTIC_MODES = ["constellation", "cylinder", "torus"]
INFO_MODES = ["feature-space", "timeline"]
HARMONIC_INPUTS = ["andesana", "pachelbel", "integraation", "strasbourgeoise"]
PALETTE_BY_INPUT = {"andesana": "ice", "pachelbel": "ember", "incea": "aurora",
                    "integraation": "bone", "strasbourgeoise": "ember"}


def _style(inp: str) -> dict:
    """Shared look: per-track palette + timbre saturation + bloom + sensible polyphony."""
    return {
        "palette": PALETTE_BY_INPUT.get(inp, "ice"),
        "color_by": "pitch",
        "sat_by_timbre": 1,
        "bloom": 1,
        "max_peaks": 6 if inp == "incea" else 8,
    }


def build_compound() -> dict:
    out: dict = {}
    fr = lambda inp: INPUT_FRAME.get(inp, 4096)
    ttl = lambda inp: INPUT_TITLE.get(inp, inp)
    for inp in INPUTS:                                   # artistic hero renders
        for mode in ARTISTIC_MODES:
            out[f"{inp}_{mode}"] = {
                "input": inp, "mode": mode, "frame": fr(inp),
                "title": f"{ttl(inp)} — {mode}", "blurb": MODE_BLURB[mode],
                "edge_life": 60, **_style(inp),
            }
    for inp in INPUTS:                                   # information modes (own edge defaults)
        for mode in INFO_MODES:
            out[f"{inp}_{mode}"] = {
                "input": inp, "mode": mode, "frame": fr(inp),
                "title": f"{ttl(inp)} — {mode}", "blurb": MODE_BLURB[mode],
                **_style(inp),
            }
    for inp in INPUTS:                                   # PCA timbral fingerprint, all inputs
        out[f"{inp}_pca"] = {
            "input": inp, "mode": "pca", "frame": fr(inp),
            "title": f"{ttl(inp)} — PCA timbral fingerprint", "blurb": MODE_BLURB["pca"],
            "pca_features": "all", "pca_pitch_spread": 0.7 if inp == "andesana" else 0.6,
            **_style(inp),
        }
    for inp in HARMONIC_INPUTS:                          # harmonic comb, harmonic material only
        out[f"{inp}_harmonic"] = {
            "input": inp, "mode": "harmonic", "frame": fr(inp),
            "title": f"{ttl(inp)} — harmonic comb", "blurb": MODE_BLURB["harmonic"],
            "f0_method": "hps", "harmonic_max_n": 16, "harmonic_tolerance_cents": 35,
            "overtone_detach": 1, **_style(inp), "max_peaks": 12,
        }
    return out


# Grid first, then compound hero renders (override the grid), then stereo presets.
EXPERIMENTS = {**build_grid(), **build_compound(), **PRESETS}

# The curated batch the current session renders (used by render.py --batch).
COMPOUND_BATCH = list(build_compound().keys())


def get(name: str) -> dict:
    if name not in EXPERIMENTS:
        raise KeyError(name)
    return EXPERIMENTS[name]


def names() -> list[str]:
    return list(EXPERIMENTS.keys())
